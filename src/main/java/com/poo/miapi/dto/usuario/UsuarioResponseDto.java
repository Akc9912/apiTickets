package com.poo.miapi.dto.usuario;

public class UsuarioResponseDto {
    private int id;
    private String nombre;
    private String apellido;
    private String email;
    private String rol;
    private boolean activo;

    // Constructor por defecto requerido por JPA
    public UsuarioResponseDto() {
    }

    // Constructor para crear un DTO a partir de un usuario
    public UsuarioResponseDto(int id, String nombre, String apellido, String email, String rol) {
        this.id = id;
        this.nombre = nombre;
        this.apellido = apellido;
        this.email = email;
        this.rol = rol;
        this.activo = true; // Por defecto, los usuarios son activos
    }

    // Getters y setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApellido() {
        return apellido;
    }

    public void setApellido(String apellido) {
        this.apellido = apellido;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }

    public boolean isActivo() {
        return activo;
    }

    public void setActivo(boolean activo) {
        this.activo = activo;
    }
}